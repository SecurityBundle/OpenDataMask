package com.github.odm.exception;

public class ODMException extends Exception {

	public ODMException() {

	}

	public ODMException(String arg0) {
		super(arg0);

	}

	public ODMException(Throwable arg0) {
		super(arg0);

	}

	public ODMException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

}
